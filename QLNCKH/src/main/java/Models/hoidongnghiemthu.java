package Models;

public class hoidongnghiemthu {
	private String mahdnt;
	private String tenhd;
	private String chutich;
	private String phanbien1;
	private String phanbien2;
	private String thuky;
	private String ngaynghiemthu;
	public hoidongnghiemthu() {
		super();
	}
	public hoidongnghiemthu(String mahdnt, String tenhd, String chutich, String phanbien1, String phanbien2,
			String thuky, String ngaynghiemthu) {
		super();
		this.mahdnt = mahdnt;
		this.tenhd = tenhd;
		this.chutich = chutich;
		this.phanbien1 = phanbien1;
		this.phanbien2 = phanbien2;
		this.thuky = thuky;
		this.ngaynghiemthu = ngaynghiemthu;
	}
	public String getMahdnt() {
		return mahdnt;
	}
	public void setMahdnt(String mahdnt) {
		this.mahdnt = mahdnt;
	}
	public String getTenhd() {
		return tenhd;
	}
	public void setTenhd(String tenhd) {
		this.tenhd = tenhd;
	}
	public String getChutich() {
		return chutich;
	}
	public void setChutich(String chutich) {
		this.chutich = chutich;
	}
	public String getPhanbien1() {
		return phanbien1;
	}
	public void setPhanbien1(String phanbien1) {
		this.phanbien1 = phanbien1;
	}
	public String getPhanbien2() {
		return phanbien2;
	}
	public void setPhanbien2(String phanbien2) {
		this.phanbien2 = phanbien2;
	}
	public String getThuky() {
		return thuky;
	}
	public void setThuky(String thuky) {
		this.thuky = thuky;
	}
	public String getNgaynghiemthu() {
		return ngaynghiemthu;
	}
	public void setNgaynghiemthu(String ngaynghiemthu) {
		this.ngaynghiemthu = ngaynghiemthu;
	}
	
	
}
