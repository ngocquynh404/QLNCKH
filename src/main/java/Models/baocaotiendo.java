package Models;

public class baocaotiendo {
	private String mabaocao;
	private String madetai;
	private String magiaidoan;
	public baocaotiendo() {
		super();
	}
	public baocaotiendo(String mabaocao, String madetai, String magiaidoan) {
		super();
		this.mabaocao = mabaocao;
		this.madetai = madetai;
		this.magiaidoan = magiaidoan;
	}
	public String getMabaocao() {
		return mabaocao;
	}
	public void setMabaocao(String mabaocao) {
		this.mabaocao = mabaocao;
	}
	public String getMadetai() {
		return madetai;
	}
	public void setMadetai(String madetai) {
		this.madetai = madetai;
	}
	public String getMagiaidoan() {
		return magiaidoan;
	}
	public void setMagiaidoan(String magiaidoan) {
		this.magiaidoan = magiaidoan;
	}
	
	
}
